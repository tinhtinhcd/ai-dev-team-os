#!/usr/bin/env node
/**
 * Sync open tickets from Linear to docs/OPEN_TICKETS.md.
 * Run: LINEAR_API_KEY=... [LINEAR_TEAM_ID=...] node scripts/sync-open-tickets.js
 *
 * Requires LINEAR_API_KEY. LINEAR_TEAM_ID is optional; if set, filters to that team.
 */
/* eslint-disable @typescript-eslint/no-require-imports -- Standalone Node script */

const fs = require("fs");
const path = require("path");

const TODO_TYPES = ["triage", "backlog", "unstarted"];
const IN_PROGRESS_TYPES = ["started"];
const DONE_TYPES = ["completed", "canceled"];

function mapStateTypeToStatus(stateType) {
  if (TODO_TYPES.includes(stateType)) return "todo";
  if (IN_PROGRESS_TYPES.includes(stateType)) return "in_progress";
  if (DONE_TYPES.includes(stateType)) return "done";
  return "todo";
}

const teamFilter = process.env.LINEAR_TEAM_ID
  ? ", team: { id: { eq: $teamId } }"
  : "";

const ISSUES_QUERY = `
  query ImportIssues($first: Int!, $after: String, $teamId: ID) {
    issues(first: $first, after: $after, filter: {
      archivedAt: { null: true }${teamFilter}
    }) {
      pageInfo { hasNextPage endCursor }
      nodes {
        identifier
        title
        description
        url
        state { type name }
      }
    }
  }
`;

async function fetchLinearIssues() {
  const apiKey = process.env.LINEAR_API_KEY;
  const teamId = process.env.LINEAR_TEAM_ID;

  if (!apiKey) {
    console.error("LINEAR_API_KEY is required. Set it in .env or pass it when running.");
    process.exit(1);
  }

  const issues = [];
  let after = null;

  do {
    const variables = { first: 250, after };
    if (teamId) variables.teamId = teamId;

    const res = await fetch("https://api.linear.app/graphql", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: apiKey.startsWith("lin_api_") ? apiKey : `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        query: ISSUES_QUERY.replace(/\s+/g, " ").trim(),
        variables,
      }),
    });

    if (!res.ok) {
      throw new Error(`Linear API error: ${res.status}`);
    }

    const json = await res.json();

    if (json.errors?.length) {
      throw new Error(json.errors[0]?.message ?? "Linear API error");
    }

    const nodes = json.data?.issues?.nodes ?? [];
    const pageInfo = json.data?.issues?.pageInfo;

    for (const node of nodes) {
      const stateType = node.state?.type ?? "unstarted";
      issues.push({
        identifier: node.identifier,
        title: node.title,
        status: mapStateTypeToStatus(stateType),
        stateName: node.state?.name,
        url: node.url,
      });
    }

    after = pageInfo?.hasNextPage ? pageInfo.endCursor ?? null : null;
  } while (after);

  return issues;
}

function buildMarkdown(issues) {
  const todo = issues.filter((i) => i.status === "todo");
  const inProgress = issues.filter((i) => i.status === "in_progress");
  const done = issues.filter((i) => i.status === "done");

  const lines = [
    "# Open Tickets",
    "",
    "> **Source:** Linear. This file is generated by `npm run sync:open-tickets`.",
    "> Run with `LINEAR_API_KEY` (and optionally `LINEAR_TEAM_ID`) set.",
    "",
    "## In Progress",
    "",
    ...(inProgress.length
      ? inProgress.map((i) => `- [ ] **${i.identifier}** — ${i.title}${i.url ? ` — [Open](${i.url})` : ""}`)
      : ["- *(none)*"]),
    "",
    "## To Do",
    "",
    ...(todo.length
      ? todo.map((i) => `- [ ] **${i.identifier}** — ${i.title}${i.url ? ` — [Open](${i.url})` : ""}`)
      : ["- *(none)*"]),
    "",
    "## Done",
    "",
    ...(done.length
      ? done.map((i) => `- [x] **${i.identifier}** — ${i.title}${i.url ? ` — [Open](${i.url})` : ""}`)
      : ["- *(none)*"]),
    "",
    "---",
    "",
    "*Last synced from Linear. Linear is the single source of truth for the backlog.*",
  ];

  return lines.join("\n");
}

async function main() {
  try {
    const issues = await fetchLinearIssues();
    const md = buildMarkdown(issues);

    const outPath = path.join(process.cwd(), "docs", "OPEN_TICKETS.md");
    fs.mkdirSync(path.dirname(outPath), { recursive: true });
    fs.writeFileSync(outPath, md, "utf8");

    console.log(`Wrote ${issues.length} issues to ${outPath}`);
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }
}

main();
